#include "philosophers.h"

void	ft_putstr(char *s)
{
	int i;
	
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
}

int main(void)
{
	ft_putstr("philosophers!\n");
	return (0);
}
